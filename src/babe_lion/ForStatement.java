package babe_lion;

public class ForStatement {
    //화폐 문제
    //
    public static void main(String[] args) {
      // 1 ~ 5까지 출력.
        // 2, 4, 6, 8, 10
      for(int i = 2; i <= 10; i+=2){
          System.out.println(i);
      }

      int a1 = 1;
      a1++;
      System.out.println(a1);

    }
}
